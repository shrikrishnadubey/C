#include<stdio.h>
int main()
{
    int i;
    float x, y;

    printf("Enter the Number i,x,j :");
    scanf("%f%d%f", &x, &i, &y);
    printf("Result:\n x = %f\n i = %d\n y = %f\n", x, i, y);
    return 0;
}

